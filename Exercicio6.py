# Solicita o valor do raio ao usuário
raio = float(input("Digite o valor do raio do círculo: "))

# Define o valor de pi
pi = 3.14

# Calcula a área e o perímetro
area = pi * raio ** 2
perimetro = 2 * pi * raio

# Exibe os resultados
print(f"Área: {area}")
print(f"Perímetro: {perimetro}")

